CREATE VIEW CRED_6
AS SELECT COD_ASIGNATURA, NOMBRE_ASIGNATURA, NOMBRE, APELLIDO
FROM ASIGNATURA A, PROFESOR P, PERSONA PE
WHERE P.DNI = PE.DNI AND P.DNI = A.DNI AND CREDITOS > 6 AND EXPERIENCIA > 3;